// 土曜だけ そろばん(C/D)の時間がちがう
const SAT_SHIFT = { '17:20':'16:10', '17:10':'16:00', '17:40':'16:30', '18:45':'18:15' };

export const toMin = (t) => { const [h, m] = t.split(':').map(Number); return h * 60 + m; };

export function buildTimeline(pattern, weather, dow) {
  let list = (TIMELINE[`${pattern}_${weather}`] || TIMELINE.F_sun).map((x) => ({ ...x }));
  if (dow === '土' && (pattern === 'C' || pattern === 'D')) {
    list = list.map((x) => {
      if (SAT_SHIFT[x.time]) {
        x.time = SAT_SHIFT[x.time];
        if (x.title.startsWith('そろばん（')) x.title = 'そろばん（16:30〜18:00）';
        x.note = x.note ? `${x.note}（土曜の時間）` : '土曜は はやめ！';
      }
      return x;
    });
    list.sort((a, b) => toMin(a.time) - toMin(b.time));
  }
  return list;
}

